  function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1)); // Generate a random index
        // Swap elements at i and j
        const temp = array[i];
        array[i] = array[j];
        array[j] = temp;
    }
}

